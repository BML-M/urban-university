example = "Hello, World!"

# Выводим первый символ строки
print(example[0])

# Выводим последний символ строки
print(example[-1])

# Выводим вторую половину строки
print(example[len(example) // 2:])

# Выводим слово наоборот
print(example[::-1])

# Выводим каждый второй символ строки
print(example[::2])
